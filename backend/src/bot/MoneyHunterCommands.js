    /**
     * Handle callback queries from inline keyboards
     */
    async handleCallbackQuery(callbackQuery) {
        const chatId = callbackQuery.message.chat.id;
        const data = callbackQuery.data;
        const userId = callbackQuery.from.id;

        // Check authorization
        if (!this.adminUsers.has(userId) && process.env.NODE_ENV !== 'development') {
            await this.bot.answerCallbackQuery(callbackQuery.id, '‚ùå Unauthorized');
            return;
        }

        try {
            if (data.startsWith('approve_')) {
                const opportunityId = data.replace('approve_', '');
                await this.moneyHunter.approveOpportunity(opportunityId);
                await this.bot.answerCallbackQuery(callbackQuery.id, '‚úÖ Opportunity approved!');
                
            } else if (data.startsWith('reject_')) {
                const opportunityId = data.replace('reject_', '');
                await this.moneyHunter.rejectOpportunity(opportunityId);
                await this.bot.answerCallbackQuery(callbackQuery.id, '‚ùå Opportunity rejected!');
            }

            // Update the message to show action taken
            await this.bot.editMessageReplyMarkup(
                { inline_keyboard: [] },
                { 
                    chat_id: chatId, 
                    message_id: callbackQuery.message.message_id 
                }
            );

        } catch (error) {
            await this.bot.answerCallbackQuery(callbackQuery.id, `‚ùå Error: ${error.message}`);
        }
    }

